package org.knime.core.data.image.png;

import java.io.IOException;
import java.io.InputStream;

import javax.imageio.ImageIO;

import org.knime.core.data.DataCell;
import org.knime.core.data.DataCellFactory.FromInputStream;
import org.knime.core.data.DataType;

/**
 * Factory for {@link PNGImageCell}s.
 *
 * @author Thorsten Meinl, KNIME.com, Zurich, Switzerland
 * @since 3.0
 */
public class PNGImageCellFactory implements FromInputStream {
    /**
     * The data type generated by this factors.
     */
    public static final DataType TYPE = PNGImageContent.TYPE;

    /**
     * {@inheritDoc}
     */
    @Override
    public DataType getDataType() {
        return PNGImageContent.TYPE;
    }

    /**
     * {@inheritDoc}
     */
    @Override
    public DataCell createCell(final InputStream input) throws IOException {
        return create(input);
    }

    /**
     * Creates a new data call containing a PNG image from the given stream. The stream will not be closed at the end.
     *
     * @param is an input stream
     * @return a new data cell
     * @throws IOException If reading from the stream fails.
     * @throws IllegalArgumentException If the argument does not represent a valid png byte stream (according to
     *             {@link ImageIO#read(InputStream)}.
     */
    public static DataCell create(final InputStream is) throws IOException {
        return new PNGImageContent(is).toImageCell();
    }

    /**
     * Creates a new data call containing a PNG image from the byte array.
     *
     * @param contents a byte array containing a PNG image
     * @return a new data cell
     * @throws IOException If reading from the stream fails.
     * @throws IllegalArgumentException If the argument does not represent a valid png byte stream (according to
     *             {@link ImageIO#read(InputStream)}.
     */
    public static DataCell create(final byte[] contents) throws IOException {
        return new PNGImageContent(contents).toImageCell();
    }
}
