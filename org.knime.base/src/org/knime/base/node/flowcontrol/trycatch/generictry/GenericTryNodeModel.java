package org.knime.base.node.flowcontrol.trycatch.generictry;

import java.io.File;
import java.io.IOException;

import org.knime.core.node.CanceledExecutionException;
import org.knime.core.node.ExecutionContext;
import org.knime.core.node.ExecutionMonitor;
import org.knime.core.node.InvalidSettingsException;
import org.knime.core.node.NodeModel;
import org.knime.core.node.NodeSettingsRO;
import org.knime.core.node.NodeSettingsWO;
import org.knime.core.node.port.PortObject;
import org.knime.core.node.port.PortObjectSpec;
import org.knime.core.node.port.PortType;
import org.knime.core.node.workflow.FlowTryCatchContext;
import org.knime.core.node.workflow.ScopeStartNode;

/**
 * Start of a Try-Catch Enclosure. Puts a TryCatchFlowLoopContext object onto
 * the stack so the end node can properly clean it up (and perform some
 * sanity checking).
 *
 * @author M. Berthold, University of Konstanz
 */
public class GenericTryNodeModel extends NodeModel implements ScopeStartNode<FlowTryCatchContext> {

//    private static final NodeLogger LOGGER = NodeLogger.getLogger(GenericTryNodeModel.class);

    /**
     * One input, one output.
     *
     * @param ptype type of ports.
     */
    protected GenericTryNodeModel(final PortType ptype) {
        super(new PortType[] {ptype}, new PortType[] {ptype});
    }

    /**
     * {@inheritDoc}
     */
    @Override
    protected PortObjectSpec[] configure(final PortObjectSpec[] inSpecs)
            throws InvalidSettingsException {

        // framework should push marker object (@see FlowTryCatchContext) onto stack
        // will be filled with information if a node failure is encountered and read
        // by the corresponding end node.

        // pass through spec.
        return new PortObjectSpec[]{inSpecs[0]};
    }

    /**
     * {@inheritDoc}
     */
    @Override
    protected PortObject[] execute(final PortObject[] inData,
            final ExecutionContext exec) throws Exception {
        // pass through port object.
        return new PortObject[]{inData[0]};
    }

    /**
     * {@inheritDoc}
     */
    @Override
    protected void saveSettingsTo(final NodeSettingsWO settings) {
    }

    /**
     * {@inheritDoc}
     */
    @Override
    protected void validateSettings(final NodeSettingsRO settings)
            throws InvalidSettingsException {
    }

    /**
     * {@inheritDoc}
     */
    @Override
    protected void loadValidatedSettingsFrom(final NodeSettingsRO settings)
            throws InvalidSettingsException {
    }

    /**
     * {@inheritDoc}
     */
    @Override
    protected void reset() {
    }

    /**
     * {@inheritDoc}
     */
    @Override
    protected void loadInternals(final File nodeInternDir,
            final ExecutionMonitor exec) throws IOException,
            CanceledExecutionException {
    }

    /**
     * {@inheritDoc}
     */
    @Override
    protected void saveInternals(final File nodeInternDir,
            final ExecutionMonitor exec) throws IOException,
            CanceledExecutionException {
    }

}
